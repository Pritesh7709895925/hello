package datatypes;

public class DatatypeOp {
	public static void main(String[] args) {
		int a = 58;
		System.out.println(a);
		String f = "oid";
		System.out.println(f);
		double s = 85.65;
		System.out.println(s);
		char r = 'g';
		System.out.println(r);
		boolean t = true;
		System.out.println(t);
		float b =96588.54f;
		System.out.println(b);
		long l = 7709895925L;
		System.out.println(l);
		
	}
}